export class User {
	constructor(id, name, surname, birthDate, gender, email, phone, pp, role, status) {
		this.id = id;
		this.name = name;
		this.surname = surname;
		this.birthDate = birthDate;
		this.gender = gender;
		this.email = email;
		this.phone = phone;
		this.pp = pp;
		this.role = role;
		this.status = status;
	}
  }
